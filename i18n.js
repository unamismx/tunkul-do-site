function applyLanguage(lang) {
  const dict = TRANSLATIONS[lang] || TRANSLATIONS.es;
  document.documentElement.lang = lang;
  document.title = dict.page_title;

  KEYS.forEach((key) => {
    const el = document.getElementById(key.replace(/_/g, '-'));
    if (el && dict[key]) el.textContent = dict[key];
  });

  const search = document.getElementById('nearbySearch');
  if (search && dict.nearby_search_placeholder) {
    search.placeholder = dict.nearby_search_placeholder;
  }

  const switcher = document.getElementById('langSwitcher');
  if (switcher) switcher.value = lang;

  const direct = dict.direct || {};
  Object.keys(direct).forEach((id) => {
    const el = document.getElementById(id);
    if (el) el.textContent = direct[id];
  });

  if (dict.room_sheet_kicker) {
    document.querySelectorAll('.room-copy .kicker').forEach((el) => {
      el.textContent = dict.room_sheet_kicker;
    });
  }

  if (dict.room_link) {
    document.querySelectorAll('.suite-link').forEach((el) => {
      el.textContent = dict.room_link;
    });
  }

  const placeDict = PLACE_TAG_TRANSLATIONS[lang] || PLACE_TAG_TRANSLATIONS.es;
  document.querySelectorAll('.place-tag').forEach((el) => {
    const baseLabel = (el.dataset.baseLabel || el.textContent).replace(/\s+/g, ' ').trim();
    el.dataset.baseLabel = baseLabel;
    const label = placeDict[baseLabel] || baseLabel;
    el.innerHTML = '<span class="glyph"></span>' + label;
  });

  document.querySelectorAll('#nearbyGrid .nearby-card h3').forEach((el) => {
    const baseTitle = el.dataset.baseTitle || el.textContent.trim();
    el.dataset.baseTitle = baseTitle;
    const translated = NEARBY_COLUMN_TRANSLATIONS[lang]?.[baseTitle];
    el.textContent = translated || baseTitle;
  });

  document.querySelectorAll('#nearbyGrid .place-item').forEach((item) => {
    const businessName = item.querySelector('strong')?.textContent?.trim();
    const desc = item.querySelector('p');
    const route = item.querySelector('a');

    if (businessName && desc) {
      const baseDesc = desc.dataset.baseDesc || desc.textContent.trim();
      desc.dataset.baseDesc = baseDesc;
      const translatedDesc = PLACE_DESC_TRANSLATIONS[lang]?.[businessName];
      desc.textContent = translatedDesc || baseDesc;
    }

    if (route) {
      route.textContent = dict.nearby_route_label || 'Cómo llegar';
    }
  });

  localStorage.setItem(I18N_KEY, lang);
}

const initialLang = localStorage.getItem(I18N_KEY) || 'es';
applyLanguage(initialLang);

document.getElementById('langSwitcher')?.addEventListener('change', (event) => {
  applyLanguage(event.target.value);
});
